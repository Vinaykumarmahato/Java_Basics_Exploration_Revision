public class LogicalOperator1 {
    
    public static void main(String[] args) {
    
         /*
         * AND = &&
         * OR = ||
         * NOT = !
         */

        int a=10;
        int b=20;
        int c=30;

        System.out.println(a==b && a==c && b==c);
        System.out.println(a==b || a==c || b==c);
        System.out.println(a!=b);


       
    }
}
