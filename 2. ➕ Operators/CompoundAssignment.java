public class CompoundAssignment {
    
    public static void main(String[] args) {
        
        int a=10;
        a=a+4;
        System.out.println("a= " + a);

        int b=10;
        b+=10;
        System.out.println(b);

        int c=10;
        c/=5;
        System.out.println(c);

    }
}
