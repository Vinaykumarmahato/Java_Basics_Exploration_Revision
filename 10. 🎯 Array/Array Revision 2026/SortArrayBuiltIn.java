
public class SortArrayBuiltIn {

    public static void main(String[] args) {
        // sort - Ascending order
        int arr[]={8,3,6,9,-4,-67};

        print(arr);

    }

    public static void print(int[]arr){

        for(int i=0;i<arr.length;i++){
            System.out.print(arr[i]+" ");
        }
        System.out.println();
    }
}